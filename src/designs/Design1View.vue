import myheadComponent from "../components/myheadComponent.vue";
export default {
  name: "Design1View",

  data() {
    return {
      sysInfo:'分享信息',
      shareUrl:'http://localhost:8080/#/designView'
    };
  },
  components: { myheadComponent },
  methods: {
    toProtile(){
      this.$router.push({path:'/portfolio'});
    },
    shareQQ(){
       //跳转地址
       window.open(
        "https://sns.qzone.qq.com/cgi-bin/qzshare/cgi_qzshare_onekey?url=" +
          encodeURIComponent(this.shareUrl) +
          "&title=" +
          this.sysInfo +
          "&summary=" +
          this.sysInfo
      );
    },
    shareWeChat(){
      localStorage.setItem("shareUrl", this.shareUrl); // 存储当前页面地址，前端页面直接根据URL链接生成二维码
      let url = location.href.split("#")[0] + "#/toWechat"; // 注意：#/Share/toWechat: 自己定义的路由
      window.open(url);
    }
  },
  mounted() {
    window.addEventListener("scroll", () => {
      var intros = document.querySelectorAll(".pic");
      setTimeout(() => {
        intros.forEach((item, index) => {
          var itemTop = item.offsetTop;
          var itemHeight = item.offsetHeight;
          var shouldFloat = itemTop - window.scrollY < itemHeight;
          if (shouldFloat) {
            intros[index].classList.add("float");
          } else {
            intros[index].classList.remove("float");
          }
        });
      }, 120);
    });
  },
};
</script>

